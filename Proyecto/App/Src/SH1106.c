void OLED_Init(I2C_Handler_t *ptrHandlerI2C){
	config_SysTick_ms(0); // 16MHz

	delay_ms(1);

	/* Generamos la condición de Start*/
	i2c_startTransaction(ptrHandlerI2C);

	/* SET I2C SLAVE WRITE ADDRESS*/
	i2c_sendSlaveAddressRW(ptrHandlerI2C, SH1106_I2C_ADDRESS_1, I2C_WRITE_DATA);

	/* SET TYPE TO COMMAND STREAM */
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CONTROL_BYTE_CMD_STREAM);

	/* DISPLAY OFF*/
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_DISPLAY_OFF);

	/* SET COLUMN ADDRESS LOWER (2)
	 * BECAUSE THIS CONTROLLER HAS RAM SIZE 132 X 64 WHEREAS OUR DISPLAY
	 * IS 128 X 4. SO THE OLED IS MAPPED FROM RAM COLUMN 2 - COLUMN 130 */
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_COLUMN_LOWER_4 | 2);
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_COLUMN_UPPER_4 | 0);

	//SET START PAGE ADDRESS = 0
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_PAGE_ADDRESS | 0);

	//SET COMMON OUTPUT SCAN DIRECTION = TOP -> BOTTOM
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_COMMON_SCAN_DIRECTION | 8);

	//SET DISPLAY START LINE = 0
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_DISPLAY_START_LINE | 0);

	//SET CONTRAST
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_CONTRAST_CONTROL_MODE);
	i2c_sendDataByte(ptrHandlerI2C, 0x7F);

	//SET SEGMENT REMAP
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_SEGMENT_REMAP | 1);

	//SET DISPLAY = NORMAL
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_DISPLAY_NORMAL);

	//SET MULTIPLEX RATIO = 63 (ALL ROWS)
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_MULTIPLEX_RATIO);
	i2c_sendDataByte(ptrHandlerI2C, 0x3F);

	//SET ENTIRE DISPLAY = ON
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_ENTIRE_DISPLAY_ON);

	//SET DISPLAY OFFSET = 0
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_DISPLAY_OFFSET_MODE);
	i2c_sendDataByte(ptrHandlerI2C, 0x00);

	//SET DISPLAY OSCILLATOR FREQUENCY
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_OSCILLATOR_FREQUENCY);
	i2c_sendDataByte(ptrHandlerI2C, 0xF0);

	//SET DISCHARGE-PRECHARGE PERIOD
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_DISCHARGE_PRECHARGE);
	i2c_sendDataByte(ptrHandlerI2C, 0x22);

	//SET COMMON PADS HARDWARE CONFIG
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_COMMON_PADS_HARDWARE_CONFIG);
	i2c_sendDataByte(ptrHandlerI2C, 0x12);

	//SET COMMON PAD OUTPUT VOLTAGE
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_COMMON_PADS_OUTPUT_VOLTAGE);
	i2c_sendDataByte(ptrHandlerI2C, 0x20);

//	i2c_sendDataByte(ptrHandlerI2C, 0x8D);
//	i2c_sendDataByte(ptrHandlerI2C, 0x14);

	ClearScreen();

	//SET DISPLAY ON
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_DISPLAY_ON);

	/* Generamos la condicion de Stop, para que el slave se detenga despues de 1 byte*/
	i2c_stopTransaction(ptrHandlerI2C);

	delay_ms(100);

}

void DrawPixel(uint8_t x, uint8_t y, uint8_t color){
	//SET OR UNSET A PIXEL AT THE SPECIFIED X,Y LOCATION

	if((x > SH1106_I2C_OLED_MAX_COLUMN) || (y > (SH1106_I2C_OLED_MAX_PAGE * 8 + 7))){
		//PIXEL OUT OF RANGE
		return;
	}

	//PIXEL IN RANGE
	if(!color){
		//CLEAR PIXEL
		buffer[((y/8) * 128) + x] &= ~(1 << (y%8));
	}
	else{
		//DRAW PIXEL
		buffer[((y/8) * 128) + x] |= (1 << (y%8));
	}
}

void ClearScreen(void) {
	uint16_t x = 0;
	uint16_t y = 0;
	for(y = 0; y < 64; y++){
		for(x = 0; x < 128; x++){
			DrawPixel(x, y, 0);
		}
	}
}

void UpdateDisplay(I2C_Handler_t *ptrHandlerI2C)
{
	//TRANSFER THE FRAMEBUFFER TO THE DISPLAY IN BULK

	uint16_t counter = 0;
	uint16_t x = 0;
	uint16_t y = 0;

	for(y = 0; y < (SH1106_I2C_OLED_MAX_PAGE + 1); y++){
		//SET CURSOR TO THE BEGINNING OF THE CURRENT PAGE
		/* Generamos la condición de Start*/
		i2c_startTransaction(ptrHandlerI2C);

		/* SET I2C SLAVE WRITE ADDRESS*/
		i2c_sendSlaveAddressRW(ptrHandlerI2C, SH1106_I2C_ADDRESS_1, I2C_WRITE_DATA);

		/* SET TYPE TO COMMAND STREAM */
		i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CONTROL_BYTE_CMD_STREAM);

		//SET COLUMN TO 2
		i2c_sendDataByte(ptrHandlerI2C,SH1106_I2C_CMD_SET_COLUMN_UPPER_4);
		i2c_sendDataByte(ptrHandlerI2C,SH1106_I2C_CMD_SET_COLUMN_LOWER_4 | 2);

		//SET PAGE
		i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_PAGE_ADDRESS | y);

		/* Generamos la condicion de Stop, para que el slave se detenga despues de 1 byte*/
		i2c_stopTransaction(ptrHandlerI2C);


		//SEND PAGE DATA
		/* Generamos la condición de Start*/
		i2c_startTransaction(ptrHandlerI2C);
		/* SET I2C SLAVE WRITE ADDRESS*/
		i2c_sendSlaveAddressRW(ptrHandlerI2C, SH1106_I2C_ADDRESS_1, I2C_WRITE_DATA);

		i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CONTROL_BYTE_DATA_STREAM);

		for(x = 0; x < (SH1106_I2C_OLED_MAX_COLUMN + 1); x++){
			i2c_sendDataByte(ptrHandlerI2C, buffer[counter]);
			counter++;
		}
		/* Generamos la condicion de Stop, para que el slave se detenga despues de 1 byte*/
		i2c_stopTransaction(ptrHandlerI2C);
	}

}

void SetDisplayOnOff(I2C_Handler_t *ptrHandlerI2C, uint8_t on){
	/* Generamos la condición de Start*/
	i2c_startTransaction(ptrHandlerI2C);

	/* SET I2C SLAVE WRITE ADDRESS*/
	i2c_sendSlaveAddressRW(ptrHandlerI2C, SH1106_I2C_ADDRESS_1, I2C_WRITE_DATA);

	//SET TYPE TO COMMAND STREAM
	i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CONTROL_BYTE_CMD_STREAM);

	if(on){
		i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_DISPLAY_ON);
	}
	else {
		i2c_sendDataByte(ptrHandlerI2C, SH1106_I2C_CMD_SET_DISPLAY_OFF);
	}

	/* Generamos la condicion de Stop, para que el slave se detenga despues de 1 byte*/
	i2c_stopTransaction(ptrHandlerI2C);

	delay_ms(100);
}

